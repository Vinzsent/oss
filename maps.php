                    weight: 2,
                    fillOpacity: 0.2
                }).addTo(map);
                L.marker(barangay.coordinates).addTo(map)
                    .bindPopup(selectedBarangay.charAt(0).toUpperCase() + selectedBarangay.slice(1));
            } else {
                map.setView([7.028012, 125.447948], 12);
            }

            // Update sitio options
            updateSitioOptions();
        }

        function updateSitioOptions() {
            const sitioDropdown = document.getElementById('frequency');
            const barangay = document.getElementById('notificationType').value;

            const sitios = barangays[barangay]?.sitios || [];

            sitioDropdown.innerHTML = '<option>Select Sitio</option>'; // Clear previous options

            for (const sitio in sitios) {
                const option = document.createElement('option');
                option.value = sitio;
                option.textContent = sitio;
                sitioDropdown.appendChild(option);
            }
        }

        function focusSitio() {
